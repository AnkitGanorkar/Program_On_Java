package com.day26;

public class Area_of_Triangle {
	// Java Program to Find Area of Triangle Using Static Value
	public static void main(String args[]) { // width of triangle
		double width = 20.0;
		// hight of triangle
		double height = 100.5;
		// finding area
		double area = (width * height) / 2;
		// printing the area
		System.out.println("Area of Triangle: " + area);
	}
}
